from __future__ import annotations

import argparse
import json
import os
from pathlib import Path
from typing import Any, Dict, List

from tqdm import tqdm

from src.gout_eval.evaluation.judge import GPTJudge, JudgeConfig


def load_jsonl(path: str | Path) -> List[Dict[str, Any]]:
    records: List[Dict[str, Any]] = []
    with Path(path).open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                records.append(json.loads(line))
    return records


def save_jsonl(path: str | Path, data: List[Dict[str, Any]]) -> None:
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        for item in data:
            f.write(json.dumps(item, ensure_ascii=False) + "\n")


def _extract_model_name(sample: Dict[str, Any]) -> str | None:
    if "model_name" in sample:
        return sample["model_name"]
    meta = sample.get("meta", {})
    if isinstance(meta, dict):
        return meta.get("model_name")
    return None


def stage_judge(
    artifacts_path: str | Path,
    output_path: str | Path,
    *,
    api_key: str | None = None,
    judge_model: str = "gpt-5",
) -> None:
    data = load_jsonl(artifacts_path)
    judge = GPTJudge(
        api_key=api_key,
        config=JudgeConfig(model_name=judge_model),
    )

    results: List[Dict[str, Any]] = []

    for sample in tqdm(data, desc="Judging"):
        try:
            question = sample["question"]
            ground_truth = sample.get("ground_truth", "")
            answer = sample.get("answer", "")
            contexts = sample.get("contexts", [])
            risk_level = str(sample.get("risk_level", "unknown"))

            judge_output = judge.judge(
                question=question,
                ground_truth=ground_truth,
                answer=answer,
                contexts=contexts,
                risk_level=risk_level,
            )

            result = {
                "run_id": sample.get("run_id"),
                "question_id": sample.get("question_id"),
                "conversation_id": sample.get("conversation_id"),
                "turn_id": sample.get("turn_id"),
                "scenario": sample.get("scenario"),
                "dataset_label": sample.get("dataset_label"),
                "risk_level": sample.get("risk_level") or sample.get("cap_do") or sample.get("category") or "unknown",
                "model_name": _extract_model_name(sample),
                "judge_model": judge_model,
                "judge_output": judge_output,
            }
        except Exception as exc:
            result = {
                "run_id": sample.get("run_id"),
                "question_id": sample.get("question_id"),
                "conversation_id": sample.get("conversation_id"),
                "turn_id": sample.get("turn_id"),
                "scenario": sample.get("scenario"),
                "dataset_label": sample.get("dataset_label"),
                "risk_level": sample.get("risk_level") or sample.get("cap_do") or sample.get("category") or "unknown",
                "model_name": _extract_model_name(sample),
                "judge_model": judge_model,
                "error": str(exc),
            }

        results.append(result)

    save_jsonl(output_path, results)
    print(f"[OK] Saved judge results to: {output_path}")


def main() -> None:
    parser = argparse.ArgumentParser(description="Run LLM-as-a-Judge on artifacts.jsonl")
    parser.add_argument(
        "--artifacts_path",
        type=str,
        required=True,
        help="Path to artifacts.jsonl generated by the pipeline.",
    )
    parser.add_argument(
        "--output_path",
        type=str,
        required=True,
        help="Path to save judge results JSONL.",
    )
    parser.add_argument(
        "--judge_model",
        type=str,
        default="gpt-5",
        help="OpenAI judge model name.",
    )
    parser.add_argument(
        "--api_key",
        type=str,
        default=os.getenv("OPENAI_API_KEY"),
        help="OpenAI API key. Defaults to OPENAI_API_KEY env var.",
    )
    args = parser.parse_args()

    stage_judge(
        artifacts_path=args.artifacts_path,
        output_path=args.output_path,
        api_key=args.api_key,
        judge_model=args.judge_model,
    )


if __name__ == "__main__":
    main()
